/**
 * Retorna as keywords traduzidas de uma carta.
 * Se o locale é espanhol, retorna as keywords originais.
 * Se não encontrar tradução para alguma keyword, retorna o original.
 */
export function getCardKeywords(card: TarotCard, locale: Locale): string[] {
  if (locale === 'es') return card.keywords;
  
  return card.keywords.map((kw) => {
    const translation = keywordTranslations[kw];
    return translation?.[locale] || kw;
  });
}
